/**
 * 測試 AI 自動生成行程功能 - 完整流程（含自動儲存）
 * 直接呼叫後端函數進行測試
 */

import dotenv from 'dotenv';
dotenv.config();

// 直接導入後端函數
import { quickExtractTourInfo } from './server/webScraper.ts';
import { getDb } from './server/db.ts';
import { tours } from './drizzle/schema.ts';
import { eq } from 'drizzle-orm';

const testUrl = 'https://travel.liontravel.com/detail?NormGroupID=972eecc0-3da1-4b60-bb1e-f600b5d6dc78&Platform=APP&GroupID=26SK207CIT-T';

async function testAutoSave() {
  console.log('='.repeat(60));
  console.log('測試 AI 自動生成行程功能（含自動儲存）');
  console.log('='.repeat(60));
  console.log('');
  console.log('測試網址:', testUrl);
  console.log('');
  
  const startTime = Date.now();
  
  try {
    // Step 1: 快速提取行程資訊
    console.log('Step 1: 快速提取行程資訊...');
    const extractedData = await quickExtractTourInfo(testUrl);
    
    const extractTime = Date.now();
    console.log(`✅ 提取完成！耗時: ${((extractTime - startTime) / 1000).toFixed(1)} 秒`);
    console.log('');
    
    // Step 2: 轉換資料格式並儲存到資料庫
    console.log('Step 2: 儲存到資料庫...');
    
    const tourData = {
      title: extractedData.basicInfo?.title || '未命名行程',
      description: extractedData.basicInfo?.description || '',
      departureCountry: extractedData.location?.departureCountry || '台灣',
      departureCity: extractedData.location?.departureCity || '台北',
      destinationCountry: extractedData.location?.destinationCountry || '',
      destinationCity: extractedData.location?.destinationCity || '',
      duration: extractedData.duration?.days || 1,
      price: extractedData.pricing?.price || 0,
      imageUrl: extractedData.imageUrl || null,
      // status 使用預設值 'active'
      isAutoGenerated: true,
      sourceUrl: testUrl,
      createdBy: 1, // Admin user ID
      
      // 額外資訊 - 轉換為 JSON 字串
      highlights: JSON.stringify(extractedData.highlights || []),
      dailyItinerary: JSON.stringify(extractedData.dailyItinerary || []),
      attractions: JSON.stringify(extractedData.attractions || []),
      includes: JSON.stringify(extractedData.pricingDetails?.includes || []),
      excludes: JSON.stringify(extractedData.pricingDetails?.excludes || []),
      optionalTours: JSON.stringify(extractedData.pricingDetails?.optionalTours || []),
      
      // 住宿資訊
      hotelName: extractedData.accommodation?.hotelName || null,
      hotelGrade: extractedData.accommodation?.hotelGrade || null,
      hotelNights: extractedData.accommodation?.hotelNights || null,
      hotelLocation: extractedData.accommodation?.hotelLocation || null,
      hotelDescription: extractedData.accommodation?.hotelDescription || null,
      hotelFacilities: JSON.stringify(extractedData.accommodation?.hotelFacilities || []),
      
      // 航班資訊
      outboundAirline: extractedData.flight?.outbound?.airline || null,
      outboundFlightNo: extractedData.flight?.outbound?.flightNo || null,
      inboundAirline: extractedData.flight?.inbound?.airline || null,
      inboundFlightNo: extractedData.flight?.inbound?.flightNo || null,
      
      // 備註
      specialReminders: extractedData.notes?.specialReminders || null,
      notes: extractedData.notes?.notes || null,
      safetyGuidelines: extractedData.notes?.safetyGuidelines || null,
      
      // 其他
      destinationDescription: extractedData.location?.destinationDescription || null,
      promotionText: extractedData.basicInfo?.promotionText || null,
      tags: JSON.stringify(extractedData.basicInfo?.tags || []),
    };
    
    // 插入資料庫
    const db = await getDb();
    if (!db) {
      throw new Error('無法連接資料庫');
    }
    const [newTour] = await db.insert(tours).values(tourData).$returningId();
    
    const saveTime = Date.now();
    console.log(`✅ 儲存完成！耗時: ${((saveTime - extractTime) / 1000).toFixed(1)} 秒`);
    console.log('');
    
    // Step 3: 驗證儲存結果
    console.log('Step 3: 驗證儲存結果...');
    
    const [savedTour] = await db.select().from(tours).where(eq(tours.id, newTour.id));
    
    console.log('');
    console.log('='.repeat(60));
    console.log('✅ 測試成功！');
    console.log('='.repeat(60));
    console.log('');
    console.log('📋 已儲存的行程資訊:');
    console.log(`  ID: ${savedTour.id}`);
    console.log(`  標題: ${savedTour.title}`);
    console.log(`  目的地: ${savedTour.destinationCountry} ${savedTour.destinationCity}`);
    console.log(`  天數: ${savedTour.duration} 天`);
    console.log(`  價格: NT$ ${savedTour.price?.toLocaleString() || 0}`);
    console.log(`  自動生成: ${savedTour.isAutoGenerated ? '是' : '否'}`);
    console.log(`  來源網址: ${savedTour.sourceUrl}`);
    console.log(`  建立時間: ${savedTour.createdAt}`);
    console.log('');
    
    const totalTime = Date.now();
    console.log(`⏱️ 總耗時: ${((totalTime - startTime) / 1000).toFixed(1)} 秒`);
    console.log('');
    console.log('🎉 行程已成功儲存到資料庫！');
    console.log('   請前往管理後台 → 行程管理 查看新增的行程');
    
  } catch (error) {
    console.log('');
    console.log('='.repeat(60));
    console.log('❌ 測試失敗！');
    console.log('='.repeat(60));
    console.log('');
    console.log('錯誤訊息:', error.message);
    console.log('');
    if (error.stack) {
      console.log('錯誤堆疊:');
      console.log(error.stack);
    }
  }
  
  process.exit(0);
}

testAutoSave();
