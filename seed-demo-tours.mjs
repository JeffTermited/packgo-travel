async function seedDemoTours() {
  const conn = await createConnection(DATABASE_URL);
  
  try {
    console.log('Checking existing demo tours...');
    const [existing] = await conn.execute(
      "SELECT productCode FROM tours WHERE productCode LIKE 'DEMO-%'"
    );
    
    const existingCodes = existing.map(r => r.productCode);
    console.log('Existing demo tours:', existingCodes);
    
    for (const tour of demoTours) {
      if (existingCodes.includes(tour.productCode)) {
        console.log(`Skipping ${tour.productCode} (already exists)`);
        continue;
      }
      
      const now = new Date();
      await conn.execute(
        `INSERT INTO tours (
          productCode, title, destination, destinationCountry, destinationCity,
          duration, price, priceCurrency, category, status, featured, description,
          heroSubtitle, imageUrl, heroImage, tags, highlights, includes, excludes,
          colorTheme, isAutoGenerated, createdBy, createdAt, updatedAt
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`,
        [
          tour.productCode, tour.title, tour.destination,
          tour.destinationCountry, tour.destinationCity,
          tour.duration, tour.price, tour.currency,
          tour.category, tour.status, tour.featured, tour.description,
          tour.heroSubtitle, tour.imageUrl, tour.heroImage,
          tour.tags, tour.highlights, tour.includes, tour.excludes,
          tour.colorTheme, tour.isAutoGenerated, 630001, now, now
        ]
      );
      console.log(`✅ Created: ${tour.productCode} - ${tour.title}`);
    }
    
    console.log('\nDone! All demo tours created.');
  } finally {
    await conn.end();
  }
}

seedDemoTours().catch(console.error);
