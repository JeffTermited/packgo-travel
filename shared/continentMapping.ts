// 洲別顯示順序
export const continentOrder = ["亞洲", "歐洲", "大洋洲", "美洲", "非洲", "中東", "其他"];

// 根據國家獲取洲別
export function getContinent(country: string): string {
  // 直接查找
  if (continentMapping[country]) {
    return continentMapping[country];
  }
  
  // 模糊匹配
  for (const [key, continent] of Object.entries(continentMapping)) {
    if (country.includes(key) || key.includes(country)) {
      return continent;
    }
  }
  
  return "其他";
}

// 將目的地列表按洲別分組
export function groupDestinationsByContinent(
  destinations: { country: string; count: number }[]
): Record<string, { country: string; count: number }[]> {
  const grouped: Record<string, { country: string; count: number }[]> = {};
  
  for (const dest of destinations) {
    const continent = getContinent(dest.country);
    if (!grouped[continent]) {
      grouped[continent] = [];
    }
    grouped[continent].push(dest);
  }
  
  // 按洲別順序排序
  const sortedGrouped: Record<string, { country: string; count: number }[]> = {};
  for (const continent of continentOrder) {
    if (grouped[continent] && grouped[continent].length > 0) {
      sortedGrouped[continent] = grouped[continent];
    }
  }
  
  return sortedGrouped;
}
