// Helper functions
function getCategoryLabel(category: string) {
  const labels: Record<string, string> = {
    group: "團體旅遊",
    custom: "客製化行程",
    package: "自由行套裝",
    cruise: "郵輪旅遊",
    theme: "主題旅遊",
  };
  return labels[category] || category;
}

function getStatusLabel(status: string) {
  const labels: Record<string, string> = {
    active: "上架中",
    inactive: "已下架",
    soldout: "已售完",
  };
  return labels[status] || status;
}
